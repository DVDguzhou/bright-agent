// schoolToCity returns city, province, and a specific location name for a given school name.
// location is the matched university key (for campus-level map pins); empty for non-university profiles.
func schoolToCity(school string) (city, province, location string) {
	s := strings.TrimSpace(school)
	if s == "" {
		return "", "", ""
	}
	if info, ok := schoolCityMap[s]; ok {
		return info[0], info[1], s
	}
	for k, info := range schoolCityMap {
		if strings.Contains(s, k) || strings.Contains(k, s) {
			return info[0], info[1], k
		}
	}
	if info, ok := nonUniSchoolCity[s]; ok {
		return info[0], info[1], ""
	}
	return "", "", ""
}
